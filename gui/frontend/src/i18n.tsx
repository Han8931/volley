const CATALOGS: Record<Locale, Dict> = { en, ko, ja };

const STORAGE_KEY = "volley.locale";

function initialLocale(): Locale {
  const saved = localStorage.getItem(STORAGE_KEY) as Locale | null;
  if (saved && saved in CATALOGS) return saved;
  // Fall back to the OS/browser preference before English.
  const tag = (navigator.language || "en").slice(0, 2).toLowerCase();
  return (tag in CATALOGS ? tag : "en") as Locale;
}

export type TFunc = (key: string, vars?: Record<string, string | number>) => string;

interface I18nValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: TFunc;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(initialLocale);

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nValue>(() => {
    const dict = CATALOGS[locale];
    const t: TFunc = (key, vars) => {
      // Missing keys fall back to English, then to the key itself — a gap in
      // one catalog degrades to readable English, never to a blank control.
      let s = dict[key] ?? en[key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) s = s.split(`{${k}}`).join(String(v));
      }
      return s;
    };
    return { locale, setLocale, t };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const v = useContext(I18nContext);
  if (!v) throw new Error("useI18n outside I18nProvider");
  return v;
}

// useT is the common case: just the translate function.
export function useT(): TFunc {
  return useI18n().t;
}
